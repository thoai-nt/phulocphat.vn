function custom_woocommerce_product_add_to_cart_text() {
    global $product;
     
    $product_type = $product->product_type;
     
    switch ( $product_type ) {
        case 'external':
            return __( 'Mua sản phẩm', 'woocommerce' );
        break;
        case 'grouped':
            return __( 'Xem sản phẩm', 'woocommerce' );
        break;
        case 'simple':
            return __( 'Thêm vào giỏ', 'woocommerce' );
        break;
        case 'variable':
            return __( 'Lựa chọn', 'woocommerce' );
        break;
        default:
            return __( 'Đọc thêm', 'woocommerce' );
    }
}

